$(".navi_bg").click(function () {
    if ($(".navi_wrap").hasClass("on")) {
      $(".navi_wrap").removeClass("on");
    } else {
      $(".navi_wrap").addClass("on");
    }
  });


  $(".menu_btn").click(function () {
    if ($(".navi_wrap").hasClass("on")) {
      $(".navi_wrap").removeClass("on");
    } else {
      $(".navi_wrap").addClass("on");
    }
  })

